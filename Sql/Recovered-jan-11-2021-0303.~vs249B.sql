 AND SE1.E1_CLIENTE = '003078' 
 AND SE1.E1_LOJA = '01' 
 AND convert(char,DATEADD(DAY,1,SE1.E1_VENCREA) ,112) < '20210106' 
 AND rTrim(SE1.E1_BAIXA) = '' 
 AND  SE1.E1_STATUS = 'A' 
 AND SE1.E1_VALOR >= SE1.E1_SALDO 




