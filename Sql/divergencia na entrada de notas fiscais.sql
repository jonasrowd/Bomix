--DIVERGENCIA NA ENTRADA DAS NOTAS
SELECT 

D1_COD,C7_PRODUTO, D1_VUNIT, C7_PRECO, D1_QUANT, C7_QUANT, D1_TOTAL, C7_TOTAL, D1_TES, C7_TES
,* 

FROM SD1010 D1
LEFT JOIN SCR010 CR
	ON CR.CR_FILIAL=D1.D1_FILIAL AND CR.CR_NUM=D1.D1_PEDIDO
LEFT JOIN SC7010 C7 
	ON C7.C7_FILIAL=D1.D1_FILIAL AND C7.C7_NUM=D1.D1_PEDIDO AND C7.C7_PRODUTO=D1.D1_COD 
WHERE 
	D1.D_E_L_E_T_=''
AND CR.D_E_L_E_T_=''
AND C7.D_E_L_E_T_=''
AND C7.C7_NUM='116256'
ORDER BY D1.D1_ITEM


