SELECT C7_DESCRI, A2_NOME FORNECE, C7_CC AS NATUREZA, CTT_DESC01 AS ED_DESCRIC, C7_OBS OBS, '' NOTA, C7_NUM PEDIDO, C7_DESCRI AS DESC_PEDIDO,C7_FSDTPRE EMISSAO, '' VENC, SUM(C7_TOTAL)+SUM(C7_VALIPI) AS VALOR, C7_COND CONDICAO, 'Previstos' TIPO 
               FROM SC7010
               JOIN SA2010 SA2 ON A2_COD = C7_FORNECE 
			   AND A2_LOJA = C7_LOJA 
			   AND A2_COD = '121558' 
			   AND A2_FILIAL = '010101'
			   AND SA2.D_E_L_E_T_<>'*'
			   LEFT JOIN CTT010 CTT ON C7_CC = CTT.CTT_CUSTO 
			   AND CTT.D_E_L_E_T_<>'*' 
			   AND CTT.CTT_FILIAL= '010101'
 WHERE C7_QUJE < C7_QUANT 
 AND C7_RESIDUO <> 'S' 
 AND C7_EMISSAO BETWEEN '20200101' AND '20210111' 
 AND C7_FILIAL = '010101'
 GROUP BY C7_DESCRI, C7_NUM, A2_NOME, C7_OBS, C7_FSDTPRE, C7_COND,  C7_CC